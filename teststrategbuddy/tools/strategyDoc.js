function strategyToMarkdown(issue, strategy) {
  const lines = [];
  const now = new Date().toLocaleDateString('en-GB', { year: 'numeric', month: 'long', day: 'numeric' });

  lines.push(`# QA Test Strategy — ${strategy.featureName || issue.summary}`);
  lines.push(`**Jira Issue:** ${issue.key} | **Priority:** ${issue.priority} | **Date:** ${now}`);
  lines.push('');
  lines.push('---');
  lines.push('');

  // 1. Objective
  lines.push('## 1. Objective');
  lines.push(strategy.objective || 'TBD');
  lines.push('');

  // 2. Scope
  lines.push('## 2. Scope');
  lines.push('### In Scope');
  (strategy.scope?.inScope || []).forEach(item => lines.push(`- ${item}`));
  lines.push('');
  lines.push('### Out of Scope');
  (strategy.scope?.outOfScope || []).forEach(item => lines.push(`- ${item}`));
  lines.push('');

  // 3. Focus Areas
  lines.push('## 3. Focus Areas');
  (strategy.focusAreas || []).forEach(item => lines.push(`- ${item}`));
  lines.push('');

  // 4. Approach
  lines.push('## 4. Approach & Testing Techniques');
  (strategy.approach || []).forEach(item => lines.push(`- ${item}`));
  lines.push('');

  // 5. Deliverables
  lines.push('## 5. Deliverables');
  (strategy.deliverables || []).forEach(item => lines.push(`- ${item}`));
  lines.push('');

  // 6. Team & Schedule
  lines.push('## 6. Team & Schedule');
  lines.push(`**Team Size:** ${strategy.teamAndSchedule?.teamSize || 'TBD'}`);
  lines.push(`**Duration:** ${strategy.teamAndSchedule?.duration || 'TBD'}`);
  lines.push('');
  lines.push('| Phase | Activity |');
  lines.push('|---|---|');
  (strategy.teamAndSchedule?.phases || []).forEach(p => {
    lines.push(`| ${p.phase} | ${p.activity} |`);
  });
  lines.push('');

  // 7. Entry & Exit Criteria
  lines.push('## 7. Entry & Exit Criteria');
  lines.push('### Entry Criteria');
  (strategy.entryCriteria || []).forEach(item => lines.push(`- ${item}`));
  lines.push('');
  lines.push('### Exit Criteria');
  (strategy.exitCriteria || []).forEach(item => lines.push(`- ${item}`));
  lines.push('');

  // 8. Risks
  lines.push('## 8. Risks & Mitigations');
  lines.push('| Risk | Mitigation |');
  lines.push('|---|---|');
  (strategy.risks || []).forEach(r => {
    lines.push(`| ${r.risk} | ${r.mitigation} |`);
  });
  lines.push('');

  // 9. Tools
  lines.push('## 9. Tools & Environment');
  (strategy.tools || []).forEach(item => lines.push(`- ${item}`));
  lines.push('');

  // 10. Sign-off
  lines.push('## 10. Sign-off & Approvals');
  lines.push('| Role | Name | Date |');
  lines.push('|---|---|---|');
  (strategy.approvals || []).forEach(a => {
    lines.push(`| ${a.role} | ${a.name} | ${a.date} |`);
  });
  lines.push('');
  lines.push('---');
  lines.push('*Generated by TestStrategyBuddy — powered by GROQ + Jira*');

  return lines.join('\n');
}

module.exports = { strategyToMarkdown };
