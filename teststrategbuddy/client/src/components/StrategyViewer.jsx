import React from 'react';

export default function StrategyViewer({ result, theme: t }) {
  const { issue, strategy, markdown } = result;

  function download() {
    const blob = new Blob([markdown], { type: 'text/markdown' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = `${issue.key}-test-strategy.md`;
    a.click();
    URL.revokeObjectURL(url);
  }

  return (
    <div style={styles.wrapper}>
      {/* Issue Header */}
      <div style={{ ...styles.issueBar, background: t.card, border: `1px solid ${t.border}` }}>
        <div style={styles.issueMeta}>
          <span style={{ ...styles.issueKey, color: t.accent }}>{issue.key}</span>
          <span style={{ ...styles.issueSummary, color: t.text }}>{issue.summary}</span>
        </div>
        <div style={styles.badges}>
          <Badge label={issue.priority} color={t.badge} text={t.badgeText} />
          <Badge label={issue.issueType} color={t.tag} text={t.tagText} />
          <button onClick={download} style={{ ...styles.dlBtn, background: t.accent, color: '#fff' }}>
            ⬇ Download .md
          </button>
        </div>
      </div>

      {/* Section 1: Objective */}
      <Section num="1" title="Objective" theme={t}>
        <p style={{ ...styles.prose, color: t.text }}>{strategy.objective}</p>
      </Section>

      {/* Section 2: Scope */}
      <Section num="2" title="Scope" theme={t}>
        <TwoCol
          left={{ title: '✅ In Scope', items: strategy.scope?.inScope || [] }}
          right={{ title: '🚫 Out of Scope', items: strategy.scope?.outOfScope || [] }}
          theme={t}
        />
      </Section>

      {/* Section 3: Focus Areas */}
      <Section num="3" title="Focus Areas" theme={t}>
        <TagList items={strategy.focusAreas || []} theme={t} />
      </Section>

      {/* Section 4: Approach */}
      <Section num="4" title="Approach & Testing Techniques" theme={t}>
        <BulletList items={strategy.approach || []} theme={t} />
      </Section>

      {/* Section 5: Deliverables */}
      <Section num="5" title="Deliverables" theme={t}>
        <BulletList items={strategy.deliverables || []} theme={t} />
      </Section>

      {/* Section 6: Team & Schedule */}
      <Section num="6" title="Team & Schedule" theme={t}>
        <div style={styles.metaRow}>
          <MetaItem label="Team Size" value={strategy.teamAndSchedule?.teamSize || 'TBD'} theme={t} />
          <MetaItem label="Duration" value={strategy.teamAndSchedule?.duration || 'TBD'} theme={t} />
        </div>
        {(strategy.teamAndSchedule?.phases || []).length > 0 && (
          <Table
            headers={['Phase', 'Activity']}
            rows={(strategy.teamAndSchedule.phases || []).map(p => [p.phase, p.activity])}
            theme={t}
          />
        )}
      </Section>

      {/* Section 7: Entry & Exit Criteria */}
      <Section num="7" title="Entry & Exit Criteria" theme={t}>
        <TwoCol
          left={{ title: '🟢 Entry Criteria', items: strategy.entryCriteria || [] }}
          right={{ title: '🔴 Exit Criteria', items: strategy.exitCriteria || [] }}
          theme={t}
        />
      </Section>

      {/* Section 8: Risks */}
      <Section num="8" title="Risks & Mitigations" theme={t}>
        <Table
          headers={['Risk', 'Mitigation']}
          rows={(strategy.risks || []).map(r => [r.risk, r.mitigation])}
          theme={t}
        />
      </Section>

      {/* Section 9: Tools */}
      <Section num="9" title="Tools & Environment" theme={t}>
        <TagList items={strategy.tools || []} theme={t} />
      </Section>

      {/* Section 10: Sign-off */}
      <Section num="10" title="Sign-off & Approvals" theme={t}>
        <Table
          headers={['Role', 'Name', 'Date']}
          rows={(strategy.approvals || []).map(a => [a.role, a.name, a.date])}
          theme={t}
        />
      </Section>

      <div style={{ ...styles.footer, color: t.muted }}>
        Generated by TestStrategyBuddy · powered by GROQ + Jira
      </div>
    </div>
  );
}

function Section({ num, title, children, theme: t }) {
  return (
    <div style={{ ...styles.section, background: t.card, border: `1px solid ${t.border}` }}>
      <div style={styles.sectionHeader}>
        <span style={{ ...styles.sectionNum, background: t.badge, color: t.badgeText }}>{num}</span>
        <h3 style={{ ...styles.sectionTitle, color: t.text }}>{title}</h3>
      </div>
      <div style={styles.sectionBody}>{children}</div>
    </div>
  );
}

function BulletList({ items, theme: t }) {
  return (
    <ul style={styles.list}>
      {items.map((item, i) => (
        <li key={i} style={{ ...styles.listItem, color: t.text, borderLeft: `3px solid ${t.accent}` }}>
          {item}
        </li>
      ))}
    </ul>
  );
}

function TagList({ items, theme: t }) {
  return (
    <div style={styles.tagRow}>
      {items.map((item, i) => (
        <span key={i} style={{ ...styles.tag, background: t.tag, color: t.tagText }}>{item}</span>
      ))}
    </div>
  );
}

function TwoCol({ left, right, theme: t }) {
  return (
    <div style={styles.twoCol}>
      <div style={{ flex: 1 }}>
        <div style={{ ...styles.colTitle, color: t.muted }}>{left.title}</div>
        <BulletList items={left.items} theme={t} />
      </div>
      <div style={{ flex: 1 }}>
        <div style={{ ...styles.colTitle, color: t.muted }}>{right.title}</div>
        <BulletList items={right.items} theme={t} />
      </div>
    </div>
  );
}

function Table({ headers, rows, theme: t }) {
  return (
    <div style={styles.tableWrap}>
      <table style={{ ...styles.table, borderColor: t.border }}>
        <thead>
          <tr style={{ background: t.tableHead }}>
            {headers.map((h, i) => (
              <th key={i} style={{ ...styles.th, color: t.muted, borderColor: t.border }}>{h}</th>
            ))}
          </tr>
        </thead>
        <tbody>
          {rows.map((row, i) => (
            <tr key={i} style={{ background: i % 2 === 0 ? 'transparent' : t.sectionBg }}>
              {row.map((cell, j) => (
                <td key={j} style={{ ...styles.td, color: t.text, borderColor: t.border }}>{cell}</td>
              ))}
            </tr>
          ))}
        </tbody>
      </table>
    </div>
  );
}

function MetaItem({ label, value, theme: t }) {
  return (
    <div style={{ ...styles.metaItem, background: t.sectionBg, border: `1px solid ${t.border}` }}>
      <div style={{ ...styles.metaLabel, color: t.muted }}>{label}</div>
      <div style={{ ...styles.metaValue, color: t.text }}>{value}</div>
    </div>
  );
}

function Badge({ label, color, text }) {
  return (
    <span style={{ background: color, color: text, ...styles.badgeStyle }}>{label}</span>
  );
}

const styles = {
  wrapper: { display: 'flex', flexDirection: 'column', gap: 16 },
  issueBar: {
    borderRadius: 12, padding: '16px 24px', display: 'flex',
    alignItems: 'center', justifyContent: 'space-between', flexWrap: 'wrap', gap: 12
  },
  issueMeta: { display: 'flex', alignItems: 'center', gap: 12, flexWrap: 'wrap' },
  issueKey: { fontFamily: 'monospace', fontWeight: 800, fontSize: 16 },
  issueSummary: { fontSize: 15, fontWeight: 500 },
  badges: { display: 'flex', alignItems: 'center', gap: 8, flexWrap: 'wrap' },
  badgeStyle: { padding: '4px 12px', borderRadius: 20, fontSize: 12, fontWeight: 700 },
  dlBtn: {
    padding: '8px 18px', borderRadius: 8, border: 'none',
    fontWeight: 700, fontSize: 13, cursor: 'pointer'
  },
  section: { borderRadius: 12, overflow: 'hidden' },
  sectionHeader: { display: 'flex', alignItems: 'center', gap: 12, padding: '14px 20px 10px' },
  sectionNum: {
    width: 28, height: 28, borderRadius: 8, display: 'flex',
    alignItems: 'center', justifyContent: 'center', fontWeight: 800, fontSize: 13
  },
  sectionTitle: { fontSize: 16, fontWeight: 700, margin: 0 },
  sectionBody: { padding: '0 20px 18px' },
  list: { listStyle: 'none', padding: 0, margin: 0, display: 'flex', flexDirection: 'column', gap: 6 },
  listItem: { padding: '7px 12px', borderRadius: 6, fontSize: 14, paddingLeft: 12 },
  tagRow: { display: 'flex', flexWrap: 'wrap', gap: 8 },
  tag: { padding: '5px 14px', borderRadius: 20, fontSize: 13, fontWeight: 600 },
  twoCol: { display: 'flex', gap: 24, flexWrap: 'wrap' },
  colTitle: { fontSize: 12, fontWeight: 700, textTransform: 'uppercase', letterSpacing: 0.5, marginBottom: 10 },
  tableWrap: { overflowX: 'auto' },
  table: { width: '100%', borderCollapse: 'collapse', fontSize: 14 },
  th: { padding: '10px 14px', textAlign: 'left', fontWeight: 700, fontSize: 12, textTransform: 'uppercase', letterSpacing: 0.4, border: '1px solid' },
  td: { padding: '10px 14px', border: '1px solid', lineHeight: 1.5 },
  metaRow: { display: 'flex', gap: 16, marginBottom: 16, flexWrap: 'wrap' },
  metaItem: { flex: 1, minWidth: 120, borderRadius: 8, padding: '10px 16px' },
  metaLabel: { fontSize: 11, fontWeight: 700, textTransform: 'uppercase', letterSpacing: 0.5, marginBottom: 4 },
  metaValue: { fontSize: 15, fontWeight: 600 },
  prose: { fontSize: 15, lineHeight: 1.7, margin: 0 },
  footer: { textAlign: 'center', fontSize: 12, marginTop: 8, paddingBottom: 32 }
};
