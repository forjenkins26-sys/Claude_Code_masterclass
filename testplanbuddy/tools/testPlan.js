function planToMarkdown(plan) {
  const lines = [];

  lines.push(`# ${plan.title}`);
  lines.push(`**Test Plan ID:** ${plan.testPlanId}  |  **Source Issue:** ${plan.sourceIssue}\n`);

  lines.push(`## 1. Objective\n${plan.objective}\n`);

  lines.push(`## 2. Scope`);
  lines.push(`**In Scope:**`);
  plan.scope.inScope.forEach(i => lines.push(`- ${i}`));
  lines.push(`\n**Out of Scope:**`);
  plan.scope.outOfScope.forEach(i => lines.push(`- ${i}`));
  lines.push('');

  lines.push(`## 3. Inclusions`);
  plan.inclusions.forEach(i => lines.push(`- ${i}`));
  lines.push('');

  lines.push(`## 4. Test Environments`);
  plan.testEnvironments.forEach(i => lines.push(`- ${i}`));
  lines.push('');

  lines.push(`## 5. Defect Reporting\n${plan.defectReporting}\n`);

  lines.push(`## 6. Test Strategy`);
  plan.testStrategy.forEach(i => lines.push(`- ${i}`));
  lines.push('');

  lines.push(`## 7. Schedule`);
  lines.push(`| Phase | Owner | Dates |`);
  lines.push(`|---|---|---|`);
  plan.schedule.forEach(s => lines.push(`| ${s.phase} | ${s.owner} | ${s.dates} |`));
  lines.push('');

  lines.push(`## 8. Deliverables`);
  plan.deliverables.forEach(i => lines.push(`- ${i}`));
  lines.push('');

  lines.push(`## 9. Entry Criteria`);
  plan.entryCriteria.forEach(i => lines.push(`- ${i}`));
  lines.push('');

  lines.push(`## 10. Exit Criteria`);
  plan.exitCriteria.forEach(i => lines.push(`- ${i}`));
  lines.push('');

  lines.push(`## 11. Tools`);
  plan.tools.forEach(i => lines.push(`- ${i}`));
  lines.push('');

  lines.push(`## 12. Risks & Mitigations`);
  lines.push(`| Risk | Mitigation |`);
  lines.push(`|---|---|`);
  plan.risks.forEach(r => lines.push(`| ${r.risk} | ${r.mitigation} |`));
  lines.push('');

  lines.push(`## 13. Approvals`);
  lines.push(`| Role | Name |`);
  lines.push(`|---|---|`);
  plan.approvals.forEach(a => lines.push(`| ${a.role} | ${a.name} |`));
  lines.push('');

  lines.push(`---\n*Generated by TestPlanBuddy — ${new Date().toISOString()}*`);

  return lines.join('\n');
}

module.exports = { planToMarkdown };
